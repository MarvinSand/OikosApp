// ISO 3166-1 alpha-2 → emoji flag
export function countryToFlag(code) {
  if (!code || code.length !== 2) return ''
  return code
    .toUpperCase()
    .split('')
    .map(c => String.fromCodePoint(127397 + c.charCodeAt(0)))
    .join('')
}

